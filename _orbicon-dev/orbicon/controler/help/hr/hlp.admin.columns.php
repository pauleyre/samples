<h3>Dodavanje nove rubrike</h3>
<ol>
	<li>
		U sidebar-u (klikni na upitnik koji je otvorio ovaj help) pod <span>Naslov</span> upiši naslov nove rubrike.
	</li>
	<li>
		Odaberi jeli to nova rubrika ili podrubrika neke već postojeće.
	</li>
	<li>
		Klikni na <span>Potvrdi</span>.
	</li>
</ol>

<h3>Organizirajte meni</h3>
<p>
	Ovaj dio administracije rubrika služi za odrđivanje redosljeda prikaza rubrika u menijima, 
	uređivanje i brisanje postojećih rubrika, te ulazak u podrazine prikazanih rubrika.<br /><br />
	Redosljed rubrika se mijenja na način da kliknete i zadržite klik na rubriku te ju s mišem odvučete 
	iznad ili ispod drugih rubirka u listi.<br />
	Lista se automatski azurira sa bazom podataka.<br />  
</p>

