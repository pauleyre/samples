<h3>Select menu</h3>
<p>
	To choose in which category you want to set your column, make a selection under <span>Select menu</span>. 
	All columns are available through navigation system, except columns under type <span>Internal pages</span>.
	<br /><br />
	
	You can select one of these:	
</p>


<div class="help_txt_row"><div class="orbx_help_left_col">Horizontal menu</div>
<div class="orbx_help_right_col">Column will be set under horiznotal menu.</div></div>

<div class="help_txt_row"><div class="orbx_help_left_col">Vertical menu</div>
<div class="orbx_help_right_col">Column will be set under vertical menu.</div></div>

<div class="help_txt_row"><div class="orbx_help_left_col">Boxes</div>
<div class="orbx_help_right_col">Column will be shown in BOX model, whiche can be rearange later.</div></div>

<div class="help_txt_row"><div class="orbx_help_left_col">Internal page</div>
<div class="orbx_help_right_col">
	This type of column is not available through navigatiton system. It can represent a form or any other page.
</div></div>

<h3>Add new column</h3>
<ol>
	<li>
		In sidebar (click questionmark that brought you here) type in title in <span>Title</span> field.
	</li>
	<li>
		Choose if this will be new column or subcolumn of some other that exists.
	</li>
	<li>
		Click <span>Submit</span>.
	</li>
</ol>

<h3>Organize menu</h3>
<p>
	This part of administration of columns serve for rearanging the sequence of columns inside of menu, editing 
	and deleting existing columns and entering in sublevels of column. <br /><br />
	You can change the sequence of columns just by drag-and-drop.
	After changes have been made, the list is automaticaly saved.<br />  
</p>

